import java.util.Scanner;
class week1ca2
{
	public static void main(String args[])
	{
		System.out.println("\nDivided by 3: ");
		for (int i=6; i<=11; i++)
		{
			if (i%3==0)
			System.out.print(i +", ");
		}
		System.out.println("\nDivided by 2: ");
		for (int i=2; i<=6; i++)
		{
			if (i%2==0)
			System.out.print(i +", ");
		}
		
	}
}	